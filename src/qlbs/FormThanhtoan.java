    void loadata(){
       
        
       model=ketnoi.loaddataChitet(model, new String[]{"getmaSach","gettenSach","getSoLuong","getgia","gettien"},hoadon);
        txt_sohoadon.setText(hoadon);
        txt_ma.setText(hodonban.getMaKH().getMaKH().toString());
        txt_diachi.setText(hodonban.getMaKH().getDiaChiKh());
        txt_dienthoai.setText(hodonban.getMaKH().getDienThoaiKH());
        txt_nv.setText(hodonban.getinfoNhanvien());
        txt_ngaysinh.setDate(DateUtils.parseDate(hodonban.getMaKH().getNgaySinh()));
        txt_ten.setText(hodonban.getMaKH().getTenKH());
        txt_tongtien.setText(Xuly.calculateColumnTotal(jTable1, 4));
        date.setText(hodonban.getNgayHoaDon());
        if(hodonban.getMaKH().getGioiTinh()=="Nam"){
            rd_nam.setSelected(true);
        }else rd_nu.setSelected(true);
        
    }

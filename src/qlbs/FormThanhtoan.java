    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
     
        int result = JOptionPane.showConfirmDialog(this, "Bạn có muốn xem chi tiết?", "Xác Nhận", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        
        if (result == JOptionPane.YES_OPTION) {
            hodonban.setThanhToan(true);
            ketnoi2.sua(hodonban);
            jButton1.setEnabled(false);
            trusach();
            JOptionPane.showMessageDialog(this, "Thanh toán thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
     else {
        JOptionPane.showMessageDialog(this, "Thanh toán bị hủy", "Thông báo", JOptionPane.ERROR_MESSAGE);
}
       
    }//GEN-LAST:event_jButton1ActionPerformed

